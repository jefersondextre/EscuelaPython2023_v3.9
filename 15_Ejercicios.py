'''
Aplicación 02: Suma de dos números enteros
Enunciado: dado dos números enteros, hallar la suma.

Análisis: para la solución de este problema, se requiere que el usuario
ingrese dos números enteros y el sistema realice el cálculo respectivo
para hallar la suma, para esto usara la siguiente expresión.
'''
num1 = input('Ingrese el primer número: ')
num2 = input("Ingrese el segundo número: ")

suma= int(num1) + int(num2)
print(suma)